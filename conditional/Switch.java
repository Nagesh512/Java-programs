public class Switch {

    public static void main(String args[]) {
        char ch = 'a';

        switch (ch) {
            case 'd':
                System.out.println("Samosa");
                break;
            case 'a':
                System.out.println("Panipuri");
                break;
            case 'b':
                System.out.println("momos");
                break;
            default:
                System.out.println("Wake up");
        }

    }
}
